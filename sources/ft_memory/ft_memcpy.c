#include "../../includes/libft.h"
/**
 * @brief copies n bytes from memory area src to memory area dst
 * @param dst 
 * @param src 
 * @param n 
 * @return the original value of dst
 */
void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	size_t	i;

	i = 0;
	if (!dst && !src)
		return (NULL);
	while (i < n)
	{
		((char *)dst)[i] = ((char *)src)[i];
		i++;
	}
	return (dst);
}
