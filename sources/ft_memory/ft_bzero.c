#include "../../includes/libft.h"
/**
 * @brief writes n zeroed bytes to the string s
 * @param s string to allocate
 * @param n size in bytes to allocate
 */
void	ft_bzero(void *s, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		((char *)s)[i] = '\0';
		i++;
	}
}
