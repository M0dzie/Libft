#include "../../includes/libft.h"
/**
 * @brief tests for any character for which isalpha or isdigit is true
 * @param c 
 * @return zero if the character tests false and returns non-zero if the
 * character tests true
 */
int	ft_isalnum(int c)
{
	if ((c >= '0' && c <= '9') || (c >= 'A' && c <= 'Z')
		|| (c >= 'a' && c <= 'z'))
		return (1);
	else
		return (0);
}
