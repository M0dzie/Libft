#include "../../includes/libft.h"
/**
 * @brief computes the length of the string s
 * @param s 
 * @return the number of characters that precede the terminating NULL character
 */
size_t	ft_strlen(const char *s)
{
	size_t	len;

	len = 0;
	while (s[len])
		len++;
	return (len);
}
