#include "../../includes/libft.h"
/**
 * @brief converts a lower-case letter to the corresponding upper-case letter
 * @param c 
 * @return the corresponding upper-case letter if there is one
 */
int	ft_toupper(int c)
{
	if (c >= 'a' && c <= 'z')
		return (c - 32);
	else
		return (c);
}
