#include "../../includes/libft.h"
/**
 * @brief converts an upper-case letter to the corresponding lower-case letter
 * @param c 
 * @return the corresponding lower-case letter if there is one
 */
int	ft_tolower(int c)
{
	if (c >= 'A' && c <= 'Z')
		return (c + 32);
	else
		return (c);
}
